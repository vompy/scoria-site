				<h1>Challenges of Going to Mars</h1>

				<p>At any given moment, the delay for any communications to travel the millions of miles from Mars to Earth and vice versa is between 3 and 23 minutes.</p>

				<img class="challenges-images" src="/images/domain/challenges/opposition.png">
				<h4>During an Oppositioin Mission:</h4>
				<p>Mars and the Sun are on directly opposite sides of the Earth. This results in Mars being as close to Earth as possible. All communications have a 3-minute one-way delay. For example, an answer to a question will take 6  minutes plus the time it takes to make the decision. Operating on even a short delay is not something humans are accustomed to doing.</p>

				<img class="challenges-images" src="/images/domain/challenges/conjunction.png">
				<h4>During a Conjunction Mission:</h4>
				<p> Mars and the Earth are completely obscured from one another by the Sun. This results in Mars being as far from Earth as possible. All communications have a 23-minute one-way delay in this scenario. Clever decision-making is vital when the delay is this long. The astronaut will have to keep track of events and questions from 46 minutes or more in the past.</p>

				<img class="challenges-images" src="/images/domain/challenges/journey.png">
				<h4>A Long Journey Ahead</h4>
				<p> The journey will last approximately 9 months. About 4 to 6 astronauts will live in very close quarters with bland food options and limited access to hygiene facilities. </p>
				<p>The astronauts will remain on Mars for approximately 500 days because of orbital patterns. They will be dependent on the success of supply missions traveling from Earth or food that they grow on their own.</p>

				<h4>Contamination</h4>
				<p> Breaking open a sample is difficult to do cleanly. Exposing unweathered rock or allowing rock to hit the ground can cause unknown, uncontrollable contamination. </p>
				<p>Contaminants on human-made fabrics will permanently alter any sample. Everything worn by an astronaut must be sterile. </p>

